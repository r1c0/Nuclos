package org.nuclos.server.common.pojo;

public class ADAction {

	Integer id;
	String name;
	String action;
	String description;
	String localeresourceid;
	
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getLocaleresourceid() {
		return localeresourceid;
	}
	public void setLocaleresourceid(String localeresourceid) {
		this.localeresourceid = localeresourceid;
	}
	
		
}
